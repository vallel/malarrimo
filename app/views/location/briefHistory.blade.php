    <h1 class="section-content-title">Breve historia de Guerrero Negro</h1>

    <p>El origen del nombre de Guerrero Negro se deriva del barco ballenero 'Black Warrior'. Construido en
    1825 en los astilleros de Duxbury, Massachusetts, este barco realizó, hasta 1845, viajes de caza en
    el Océano Indico y al norte del Pacífico hasta 1851. Vendido después en Honolulu navegó desde ahí
    a las costas de Baja California llegando a la Bahía de Vizcaíno; entró al canal de la laguna de
    Guerrero Negro el 28 de noviembre de 1858 a esperar las ballenas. El 10 de diciembre zarpó.
    Después de salvar la barra de la laguna el fuerte oleaje lo batió contra la barra abriéndole la quilla.
    Los restos de la nave permanecieron a la vista durante varias décadas, señalando el peligro de la
    entrada a la que dieron el nombre de Black Warrior, mismo que después se tradujo a Guerrero
    Negro, actualmente nombre de la laguna y de la ciudad.</p>

    <p>La población de Guerrero Negro nació en 1954, cuando un visionario norteamericano de nombre
    Daniel Ludwig, decidió instalar ahí una salina para abastecer la demanda de sal de la costa oeste de
    los Estados Unidos.</p>

    <p>La extracción de sal en la región comenzó a raíz del descubrimiento de depósitos de sal fósil en
    salitrales naturales constituidos por inmensas zonas de inundación en las partes bajas de las
    marismas de la Laguna Ojo de Liebre. Estos depósitos o pisos de sal se formaron por la evaporación
    natural del agua de mar que inundaba terrenos planos e impermeables durante las mareas altas.
    Durante muchos años se extrajo sal de estos depósitos de manera rudimentaria en seco, llevando la
    sal en góndolas mineras hasta un embarcadero de madera en el extremo sudeste de la laguna para
    ser cargados en pequeñas embarcaciones. Aún restan los vestigios de aquella infraestructura.</p>

    <p>A principios de la década de los cincuenta, debido a que las salinas de San Francisco, California,
    dejaron de abastecer sal a la industria productora de papel de la costa oeste de Estados Unidos, un
    grupo de empresarios norteamericanos buscó la localización más apropiada para iniciar la
    construcción de instalaciones productoras de sal y abastecer ese nicho de mercado. El Sr. Daniel K.
    Ludwig, mismo que construyó el hotel Acapulco Princess en el puerto de Acapulco México y
    presidente de National Bulk Carriers, se interesó en un proyecto para explotar las salinas de
    Guerrero Negro e inició los trámites correspondientes ante la entonces Comisión de Fomento Minero
    del Gobierno Mexicano. Así, el 7 de abril de 1954 se constituyó Exportadora de Sal S.A.</p>

    <p>En la actualidad Guerrero Negro, BCS, cuenta con +/- 17,000 habitantes y entre sus atractivos se
    encuentran: La salina a cielo abierto mas grande del mundo, Laguna Ojo de Liebre que cada año es
    visitada por la Ballena Grís que regresa para aparearse y para dar al luz el 50% de ballenas grises
    que nacen cada año, su sistema de dunas de hasta 30 metros de altura, sus marismas y sus más de
    120 especies de aves residentes y migratorias, así como formar parte del Desierto del Vizcaíno en
    donde se encuentra la más grande manifestación de Arte Rupestre del hemisferio americano.</p>

