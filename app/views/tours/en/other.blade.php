    <h1 class="section-content-title">Cochimie trails - Cave painting tours</h1>

    <p>The isolated mountain range of Sierra de San Francisco offers one of the world's greatest collections of
        indigenous cave paintings. It is known as Baja's Great Mural Region and it is believed that the paintings are
        around 4,000 years old. According to Cochimie legends, a race of giants created the murals because they appear
        on rock walls as high as 30 feet above the ground. The awe-inspiring, larger-than-life pictographs represent
        Baja California's greatest cultural heritage and are considred a protected archeological zone by the Mexican
        government. The Great Mural Region is cited on UNESCO's World Heritage List.</p>


