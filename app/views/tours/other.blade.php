    <h1 class="section-content-title">Excursiones a las pinturas rupestres (Rutas cochimies)</h1>

    <p>Las desoladas montañas de la Sierra de San Francisco ofrecen una de las más grandes colecciones de arte rupestre
        en el mundo. En esta región se encuentran más de 300 sitios registrados ante el INAH (Instituto Nacional de
        Antropología e Historia) y se cree -según las últimas investigaciones- que las pinturas oscilan alrededor de
        los 4,000 años de antigüedad. De acuerdo a las leyendas de la tribu Cochimíe, una raza de gigantes crearon
        estos murales que aparecen en paredes rocosas de hasta diez metros de altura. Esta magnifica muestra de arte
        rupestre representa la más grande herencia pictórica de Baja California y por su importancia como zona
        arqueológica es protegida con especial esmero por el Gobierno de México y forma parte del listado de
        Patrimonios Mundiales de la Humanidad inscritos por la UNESCO.</p>


