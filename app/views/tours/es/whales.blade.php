    <h2 class="section-content-subtitle">La Ballena Gris</h2>

    <p>La ballena gris es la ballena más comúnmente vista a lo largo de la costa oeste de Norte América. Se alimenta
        durante los meses de verano y otoño, principalmente en el Mar de Bering; migra a la costa Pacífica de Baja
        California, México, para procrear y criar a sus ballenatos en el invierno; y regresa al norte en la primavera.</p>


    <h2 class="section-content-subtitle">Descripción</h2>

    <p>Hay dos tipos básicos de ballenas: las dentadas y las “barbadas.” La ballena gris es una ballena “barbada.”
        La ballena gris toma su nombre de su coloración manchada. Parte de este patrón está presente desde el nacimiento,
        pero la mayor parte es causado por balanos que crecen en su piel o por áreas depigmentadas en donde han estado
        los balanos. La ballena gris llega a medir hasta 14 metros de longitud y a pesar hasta 31.5 toneladas métricas.
        En comparación, un autobús intermunicipal tiene 12 metros de largo. En promedio, las hembras adultas son de
        mayor tamaño que los machos. Son animales de sangre caliente y respiran aire; tienen pelo (limitado a pelos
        aislados alrededor de la parte anterior de la cabeza, observables en los ballenatos); y paren crías vivas,
        las cuales se alimentan de la leche de sus madres.</p>


    <h2 class="section-content-subtitle">Alimentación</h2>

    <p>En lugar de dientes, las ballenas grises tienen una fila de 138–180 placas de barbas que crecen a los lados, a
        lo largo de la encía del maxilar superior. Las barbas están hechas de queratina, un material similar al de las
        uñas humanas. Aunque parecen rígidas y lisas en su borde externo, cada barba es áspera en el borde interno de
        la boca y se adelgaza desde unos 8 cm de ancho en la línea de la encía, hasta prácticamente una punta en su
        parte distal. Las barbas están separadas aproximadamente 6 a 10 mm entre sí, y sus bordes se sobrelapan
        formando un efectivo filtro. Puesto que las ballenas grises filtran animales del lodo y el agua, sus barbas
        son más rígidas y tienen bordes más ásperos que otras ballenas barbadas, las cuales únicamente filtran animales
        del agua. Las ballenas grises se alimentan principalmente de anfípodos bentónicos (animales similares a
        camarones que viven sobre el fondo). Para alimentarse, las ballenas nadan hasta el fondo, donde excavan con la
        boca un área del tamaño de la superficie de un escritorio por unos 30 cm de profundidad. Algunas veces esto
        crea depresiones notorias en el fondo. Los anfípodos quedan atrapados en el filtro de barbas mientras que el
        lodo y la arena salen a través de las placas de barbas con el agua. A veces se puede ver agua enlodada cerca
        de la cabeza de una ballena gris cuando ésta sale a la superficie después de una inmersión de alimentación. De
        esta manera las ballenas separan los anfípodos de la arena y el lodo. Entonces usan su lengua para lamer a los
        anfípodos atrapados en los bordes de las barbas, de manera similar a como uno lamería la crema de cacahuate
        que se adhiere al paladar. De vez en cuando, las ballenas grises también se alimentan de enjambres de mísidos
        (animales similares a camarones, de natación libre y de unos 2 cm de longitud) a lo largo de promontorios
        rocosos. En ocasiones se alimentan en aguas muy someras, justo afuera de la rompiente a lo largo de playas
        arenosas de poca pendiente, y la gente frecuentemente presume que los animales se están varando. La temporada
        de alimentación de la ballena gris se extiende desde de mediados de la primavera hasta mediados del otoño.
        La mayor parte de la población se alimenta en los Mares de Bering y Chuckchi, en Alaska. Algunos animales
        (menos del 10%) se alimentan cada verano entre British Columbia y México. Muchos de los mismos individuos
        regresan año tras año a la misma área de alimentación. La alimentación en las altas latitudes es más adecuada
        durante los meses de verano debido a que los prolongados ciclos de luz solar producen altas cantidades de
        fitoplancton (algas microscópicas), el cual a su vez es consumido por el zooplancton (animales diminutos).
        En conjunto, los dos constituyen el alimento básico para la vida en el océano y estimulan el crecimiento de
        la red trófica marina, incluyendo a los anfípodos que viven sobre el fondo y a las ballenas grises. Análisis
        de los contenidos estomacales durante la época ballenera demostraron que las ballenas grises comen muy poco
        mientras migran o durante su permanencia en las áreas de crianza y procreación. Por lo tanto, muchas ballenas
        pueden vivir de tres a cinco meses sin alimentarse.</p>


    <h2 class="section-content-subtitle">Reproducción</h2>

    <p>Los nacimientos comienzan alrededor de la Navidad y alcanzan su máximo a principios de Febrero. Las hembras dan
        a luz a una sóla cría cada dos años después de que alcanzan la madurez sexual (hacia los ocho años de edad).
        Estas se aparean en años en que no están gestando. No es raro ver más de un macho compitiendo por la atención
        de una hembra en celo. No existen largos lazos de unión entre el macho y la hembra. Un macho se puede aparear
        con más de una hembra y vice-versa. El macho no tiene ninguna responsabilidad en la crianza. Los ballenatos se
        nutren de sus madres durante seis a ocho meses, adquiriendo más independencia durante el último tercio del
        período de lactancia. El dirigirse al sur hacia aguas templadas o subtropicales para la crianza es típico de
        muchas ballenas barbadas y presumiblemente ayuda a los ballenatos a conservar calor corporal. Los nacimientos
        ocurren en las lagunas y también en aguas abiertas, aunque es posible que las ballenas prefieran las lagunas
        por ser áreas protejidas, deaguas cálidas y calmadas. Los pescadores mexicanos reportan que muy pocos tiburones
        se encuentran en las lagunas cuando las ballenas están presentes. La cría, de unos 4.5 metros, nace de cola y
        pesa alrededor de 0.9 toneladas métricas. Su respiración es torpe después del nacimiento, pero a las pocas
        horas el ballenato ya está realizando inmersiones más coordinadas. Respira unas dos veces más frecuentemente
        que la madre durante lasprimeras semanas.</p>


    <h2 class="section-content-subtitle">Migración hacia el sur</h2>

    <p>La migración hacia el sur desde el Mar de Bering comienza a finales de Noviembre. Los animales viajan hacia el
        sur hasta tres lagunas de procreación y crianza en la costa oeste de Baja California, México:
        <strong>la Laguna Ojo de Liebre</strong>, la Laguna San Ignacio, y la Bahía Magdalena. El Capitán Charles
        Scammon cartografió muchas de estas áreas a mediados del Siglo XIX cuando cazaba ballenas grises. Su libro
        “Mamíferos Marinos de la Costa Noroeste” ha sido reimpreso varias veces y es una interesante lectura. Las
        hembras preñadas son las primeras en migrar, seguidas por adultos de ambos sexos y finalmente por los juveniles.
        Las ballenas viajan a una velocidad de hasta 8.5 km por hora durante su migración hacia el sur.</p>


    <h2 class="section-content-subtitle">Migración hacia el norte</h2>

    <p>Las crías son usualmente inquietas desde que tienen una semana de edad, pero raras veces inician la migración
        hacia el norte hasta que han alcanzado al menos un mes de edad. La migración se inicia a finales de Enero con
        los animales inmaduros (algunos de los cuales no han llegado hasta México), los machos adultos, y las hembras
        sin cría. Este grupo de animales no parte de una sola vez, sino que lo hace a lo largo de varios meses. Algunos
        apareamientos pueden ser observados inclusive en Abril, cuando los animales se están desplazando hacia el norte.
        Las hembras con cría son las últimas en dejar las lagunas y se mueven más despacio. Si el clima es bueno se
        pueden ver ballenas pasando a pocos metros de las salientes de la costa durante esta época. </p>

    <p>La migración completa desde las lagunas de crianza en Baja California hasta el Mar de Bering y de regreso
        comprende una distancia de 16,000 km, la más larga migración conocida para cualquier ballena. Aunque otras
        ballenas también migran entre las áreas de alimentación en las altas latitudes y las áreas más templadas de
        reproducción y crianza en las bajas latitudes, los investigadores conocen más sobre la ballena gris porque ésta
        se desplaza muy cerca de la costa. Estos desplazamientos costeros han conducido a la especulación de que las
        ballenas grises navegan permaneciendo en aguas someras o manteniendo el sonido de las olas a un lado o el otro,
        dependiendo del sentido de la migración.</p>


    <h2 class="section-content-subtitle">Explotación y conservación</h2>

    <p>La regularidad de los movimientos de las ballenas grises hizo posible que los balleneros las explotaran
        intensivamente a lo largo de su ruta migratoria y en las lagunas de crianza. Hacia 1855 se establecieron
        estaciones costeras de procesamiento en San Diego, donde la navegación dentro de la bahía era juzgada peligrosa
        por la abundancia de ballenas en reproducción. Para 1873, las ballenas grises habían sido tan diezmadas que las
        estaciones costeras fueron cerradas. La caza continuó en las lagunas mexicanas, y la población de la ballena
        gris disminuyó aún más, hasta que las lagunas fueron cerradas a la caza por el gobierno mexicano. La ballena
        gris recibió completa protección en 1937 por la Liga de Naciones y en 1946 por la recientemente conformada
        Comisión Ballenera Internacional(CBI).</p>

    <p>Después de 58 años de protección a la caza comercial, la población de la ballena gris creció lo suficiente como
        para ser removida de la Lista de Especies en Peligro de Extinción de los Estados Unidos en 1994. La población
        actual se estima en unos 23,000 individuos, cifra que se cree cercana a la población existente antes de la
        explotación ballenera. Una pequeña cuota de 176 ballenas por año es capturada a lo largo de la costa de Siberia.
        Este número fue determinado con base en el promedio del número que fue capturado durante los últimos 20 años
        del período de recuperación en el cual la población continuó creciendo. Las ballenas grises están protegidas
        en México, los Estados Unidos, y Canadá, y por la CBI. En los Estados Unidos.</p>

    <p>Los únicos predadores naturales de la ballena gris son las orcas y los grandes tiburones. Las orcas están
        presentes a lo largo de la ruta migratoria y atacan principalmente a las hembras y crías que se mueven más despacio.</p>

