    <h1 class="section-content-title">Malarrimo</h1>

    <p>“MALARRIMO” es el nombre de una playa solitaria en B.C.S donde arriba de Norte a Sur la corriente Marina
        denominada de California cuyas aguas son extremadamente frías y turbulentas, lo que hizo que el navegante
        Sebastián Vizcaíno el gran explorador Español, señalara en sus cartas de navegación como un grandioso rompeolas
        con vientos encontrados y mar embravecido que sucumbe en los pocos profundos bancos de arena que como dijo
        Neruda no se sabe si salen del mar o quieren entrar en el. A este misterioso y solitario lugar llamó Sebastián
        Vizcaíno la playa del Malarrimo.</p>

    <p>El lugar de las cosas pérdidas, porque las corrientes que vienen desde Japón y Alaska, han depositado por años,
        gran cantidad de objetos que flotando en el Mar, arriban y se quedan en la playa tales como: troncos gigantescos
        de los grandes bosques de Estados Unidos y Canadá, así como objetos marinos, cadáveres de ahogados, boyas de
        vidrio soplado hechas en Japón desde antes que existiera el plástico como flotante en fin, una gran variedad de
        objetos que por años han viajado en el mar Pacífico y han sido depositados en esta inmensa playa.</p>

    <p>En 1974 cuando tenía un año de inaugurada la Carretera Transpeninsular, por recomendaciones de Secretarios de
        Estado y de el entonces Gobernador del territorio Sur Ing. Agramont Cota, otorgó la primera Licencia de
        Restaurante Bar a Enrique Achoy López quien en ese entonces se atrevió a construir un Restaurante en lo más
        desolado de la población de Guerrero Negro. Enrique implanto una cocina de corte Gourmet y construyó una muy
        famosa discoteca que era el lugar de reunión de la población y uno que otro turista que se aventuraba a visitar
        Guerrero Negro, a cargar gasolina y a tomar obligadamente sus alimentos en el Restaurante “Malarrimo”. Para muchos
        el lugar del nombre incongruente pero lleno de objetos encontrados en la playa “Malarrimo” que ahora sirven de
        decoración y pequeño museo, ha sido visitado por muchas personalidades de todo el mundo y ha obtenido de ellos
        un merecido reconocimiento por la frescura y delicadeza de sus mariscos bañados por los mejores vinos que Enrique
        adquiere en la Ciudad de Ensenada del Valle de Guadalupe, así como una lista sorpresa de vinos importados.</p>

    <p>El Restaurante ha sido promotor de una muy importante variedad de mariscos de la Laguna, en este lugar se
        descubrió y por primera vez se sirvió el famoso Callo de almeja “mano de León” que es un clásico de la cocina
        mundial se puede decir que es un manjar “Salomónico” ya que los griegos lo consideraban en sus recetarios muy
        representativos. En “Malarrimo” nace una cocina regional, con rasgos de comida Misional hasta la influencia de
        cocinas Chinas, Japonesas, Americanas, y sobre todo Mexicanas y Española Criolla, donde se usan  ingredientes de
        producción orgánica de la localidad.</p>

    <p>Malarrimo pasó pues a ser - gracias a la cocina de Don Enrique - el lugar preferido de los viajantes y los
        visitantes, desde los despistados hasta celebridades, dignatarios e incluso mandatarios del país a los que
        también tuvo el honor de servir e incluso puso en el mapa a Guerrero Negro, no solo por sus salinas o sus
        ballenas, sino por que en el lugar se encuentra un lugar emblemático y sencillo que se dio a conocer de boca en
        boca gracias a sus visitantes.</p>

    <p>En 1978 Enrique construye también un estacionamiento de casa rodantes y en 1986 se inicia la construcción de las
        “Cabañas Don Miguelito” de las cuales se construyeron 10 habitaciones modestas pero cómodas y sobre todo muy
        limpias que te hacen sentir en una atmósfera familiar y se equilibran las buenas vibras. A la fecha el hotel
        cuenta con 22 habitaciones y mantiene su ambiente familiar y relajado.</p>

    <p>La empresa “Malarrimo” tiende al crecimiento y para los años 1989 -  Luis Enrique Achoy hijo, se especializa en
        guía naturista y en Turismo Ecológico, fundando su propia Empresa a la cual denomina “ECO-TOURS MALARRIMO”, la
        cual se dedica a realizar viajes educativos, a la Salina más grande del Mundo, a la observación de Ballena Gris,
        Mamíferos Marinos y aves en la Laguna Ojo de Liebre, pero además crea infraestructura para realizar excursiones
        al desierto y su estudio y sobre todo la visita a las Monumentales y misteriosas Pinturas Rupestres de Baja
        California. Posteriormente se han especializado en la búsqueda de locaciones y apoyo logístico para la filmación
        de reportajes, comerciales, documentales y películas; contando entre sus clientes con varias cadenas de
        televisión alrededor del mundo e importantes casas productoras de filmación al contar con un importante equipo
        de Transporte, logística y campamento para la Sierra y el Mar.</p>

    <p>Posteriormente, Nena Achoy hija de la misma familia establece adjunto al Restaurante “Malarrimo”, la tienda de
        regalos y artesanías “Casa el viejo Cactus” en la cual personalmente Nena atiende y escoge en todo el País
        una selección de muy buen gusto de souvenirs que usted puede encontrar para sus regalos, algunos tienen motivos
        de Ballena que viene a ser ya la Mascota preferida de Guerrero Negro, este rinconcito de Malarrimo es lugar
        obligado de visita para mamá.</p>

    <p>Algunos años después, Mónica abre una tienda de conveniencia en el mismo complejo para avituallar a los huéspedes
        de las casas rodantes y el hotel donde los clientes y público en general pueden adquirir hielo, refrescos y
        licores, abarrotes y otros artículos que hacen mas fácil su estancia y/o viaje.</p>

    <p>Por último, hace un par de años, Tony Achoy, el menor de los 5 hermanos, se une a Malarrimo después de haber
        cursado la Licenciatura en publicidad y mercadotecnia, inyectando al negocio familiar juventud y nuevas ideas
        que aunado a la experiencia y la herencia de Don Enrique siguen haciendo de Malarrimo el lugar obligado a
        visitar y a nosotros; la familia Achoy Cota; nos compromete a mantener el estándar de servicio y calidez que
        hasta la fecha ha caracterizado a Malarrimo.</p>

